a = input('Digite algo: ')
print('O tipo primitivo desse valor e: ', type(a))
print('So tem espacos? ', a.isspace())
print('E um numero? ', a.isnumeric())
print('E alfabetico? ', a.isalpha())
print('E alfanumerico? ', a.isalnum())
print('Esta em maiuscula? ', a.isupper())
print('Colocando em maiusculo ', a.upper())
print('Esta em minusculo? ', a.islower())
print('Esta capitalizada? ', a.istitle())
#Capitalizada e quando a primeira letra esta em maiuscula
#Nesse codigo que estamos usando a letra a antes das funcoes nos chamamos de objeto, ele tem caracteristica e realiza funcionalidade
